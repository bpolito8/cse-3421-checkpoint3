
public class LibraryCardHolder {
	int cardNumber;
	String firstName;
	String lastName;
	String address;
	String emailAddress;
	public LibraryCardHolder(int cardNumber, String firstName, String lastName, String address, String emailAddress) {
		this.cardNumber = cardNumber;
		this.firstName = firstName;
		this.lastName = lastName;
		this.address = address;
		this.emailAddress = emailAddress;
	}
}
